library IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.NUMERIC_STD.ALL;
USE work.my_pack_v2.ALL;
USE IEEE.math_real.ALL;

entity SIM_TEMP is 
	--GENERIC();
end SIM_TEMP;
 
architecture Behavioral of SIM_TEMP is
	--------------------------------------------------------------------------
	--		CONSTANTs
	--------------------------------------------------------------------------
	CONSTANT	clk_per					:	TIME	:=	10 NS;
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
	--		COMPONENTS
	--------------------------------------------------------------------------
	
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
	--		SIGNALS
	--------------------------------------------------------------------------
	SIGNAL	inp							:	std_logic_vector(7	DOWNTO	0);
	SIGNAL	int1						:	INTEGER;
	SIGNAL	int2						:	INTEGER;
	SIGNAL	int3						:	INTEGER;
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
BEGIN
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
	PROCESS
	BEGIN
		WAIT FOR clk_per;
		inp			<=	"0000XXXX";
		WAIT FOR clk_per;
		inp			<=	"000000U0";
		WAIT FOR clk_per;
		inp			<=	"0000Z000";
		WAIT FOR clk_per;
		inp			<=	"00H00000";
		WAIT FOR clk_per;
		inp			<=	"00L00000";
		WAIT FOR clk_per;
		inp			<=	"00000000";
		WAIT FOR clk_per;
		FOR i IN 0 TO 255 LOOP
			inp		<=	std_logic_vector(UNSIGNED(inp) + 1);
			WAIT FOR clk_per;
		END LOOP;
		WAIT;
	END PROCESS;
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
	int1				<=	my_to_uint(inp);
	int2				<=	my_to_uint(X"FFFF00F0");
	int3				<=	to_integer(unsigned'(X"FFFF00F0"));
	--------------------------------------------------------------------------
	--------------------------------------------------------------------------
END Behavioral;